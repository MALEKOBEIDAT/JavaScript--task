var mark;

mark = prompt( "Please input the mark(%)" );

if( mark>0 && mark<50){
    document.write("Fail")

}

else if(mark>=50 && mark<=60){
    document.write("D")


}

else if(mark>=60 && mark<=69){
    document.write("C")


}

else if(mark>=70 && mark<=79){
    document.write("B")


}

else if(mark>=80 && mark<=89){
    document.write("A")


}

else if(mark>=90 && mark<=99){
    document.write("A+")


}

else{
    document.write("Undefined")


}








